package com.joojang.bookfriend.model;


public class BookReply {


    public String username;
    public String registdate;
    public String replycategory;
    public String replycontent;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getRegistdate() {
        return registdate;
    }

    public void setRegistdate(String registdate) {
        this.registdate = registdate;
    }

    public String getReplycategory() {
        return replycategory;
    }

    public void setReplycategory(String replycategory) {
        this.replycategory = replycategory;
    }

    public String getReplycontent() {
        return replycontent;
    }

    public void setReplycontent(String replycontent) {
        this.replycontent = replycontent;
    }
}
